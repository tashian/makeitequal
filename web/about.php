  	<h3>Our Position</h3>
  	<p>When conceived, each star on the Flag of Equal Marriage represented an American state which recognizes and performs same-sex marriages. The goal was for the Flag of Equal Marriage to match the American flag, with all 50 stars lit up.</p>
  	
  	<p>On June 26th, 2015, that goal was achieved! Marriage equality is now the law of the land in America!
  	
  	<h3>Frequently Asked Questions</h3>

  	<h4>How did you decide where to put the stars?</h4>
  	
  	<p>Stars were positioned starting from the top left, based on the date each state was admitted into the union.</p>

  	<h4>What about Washington, D.C.?</h4>
  	
  	<p>Because it's not a state, Washington, D.C. doesn't have a star on the flag. We did of course include it in our list on the front page, however.</p>

  	<h4>Why are there 13 stripes on this flag, when only some of the original 13 allow equal marriage?</h4>
  	
  	<p>The 13 stripes represent the 13 colonies. Those colonies don't exist anymore, so they aren't really relevant to our campaign for marriage equality. That said, they do help relate our flag to the American flag.</p>

  	<h4>What's wrong with the rainbow flag?</h4>
  	
  	<p>Nothing at all! However, the rainbow flag is an important symbol of global LGBT pride, while the Flag of Equal Marriage addresses a specific issue: same-sex marriage in the United States. We'd be proud to see the Flag of Equal Marriage fly next to the rainbow flag!</p>

  	<h4>How can I help?</h4>
  	
